class Vowel
{
public static void main(String args[])
{
char ch= 'v';

switch(ch)
{
case 'a' :
System.out.println( ch+  " CHARECTER a entered is a  VOWEL");
break;
case 'e' :
System.out.println(ch+"  CHARECTER e entered is a  VOWEL");
break;
case 'i' :
System.out.println(ch+"  CHARECTER i entered is a  VOWEL");
break;
case 'o' :
System.out.println(ch+"  CHARECTER o entered is a  VOWEL");
break;
case 'u' :
System.out.println(ch+"  CHARECTER u entered is a  VOWEL");
break;
default : System.out.println( "is not a vowel");
break;
}
}
}
